public class Operadores{
	
	public static void main (String[] args) {
	
	System.out.println("==============================");
	System.out.println("utilizar parênteses para modificar a precedência");
	System.out.println("==============================");
	
	int com;
	int sem;
	
	sem = 2 + 6 * 5;
	System.out.println("sem parênteses, 2 + 6 * 5 é igual a " + sem);
	com = (2 + 6) * 5;
	System.out.println("com parênteses, (2 + 6) * 5 é igual a " + com);
			
			
	}
	
}	
